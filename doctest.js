let productNo = 100;
let protectionProdsDocTypesMandatory = [
  "Application Declaration",
  "Client Meeting Record",
  "FATCA CRS Entities self-certification form",
  "Enhanced Due Diligence Form",
];
let protectionProdsDocTypesOptional = ["Enhanced Due Diligence Form"];
let savingsProdsDocTypesMandatory = [
  "Application Declaration - Savings and Pension Plans",
  "Client Meeting Record - Savings",
  "FATCA CRS Entities self-certification form",
];
let savingsProdsDocTypesOptional = [
  "Enhanced Due Diligence Form",
  "Initial Quote - Savings",
  "Initial Quote - Pension Plans",
];

const getDigitalSignDocs = function() {
  if (productNo == 1) {
    documentsPolicy.map((doc) => {
      if (protectionProdsDocTypesMandatory.includes(doc.documentType)) {
        doc.isMandatory = true;
        doc.isChecked = true;
      }
    });
    documentsPolicy.map((doc) => {
      if (protectionProdsDocTypesOptional.includes(doc.documentType)) {
        doc.isMandatory = false;
        doc.isChecked = true;
      }
    });
  } else {
    documentsPolicy.map((doc) => {
      if (savingsProdsDocTypesMandatory.includes(doc.documentType)) {
        doc.isMandatory = true;
        doc.isChecked = true;
      }
    });
    documentsPolicy.map((doc) => {
      if (savingsProdsDocTypesOptional.includes(doc.documentType)) {
        doc.isMandatory = false;
        doc.isChecked = true;
      }
    });
  }

  return documentsPolicy;
};
